export function getModulesByTrack(track: Track): Module[] {
  return MODULES.filter((m) => m.track === track);
}

export function getModule(id: string): Module | undefined {
  return MODULES.find((m) => m.id === id);
}

export function getPrerequisiteModules(id: string): Module[] {
  const mod = getModule(id);
  if (!mod) return [];
  return mod.prerequisites.map((pid) => getModule(pid)).filter(Boolean) as Module[];
}
